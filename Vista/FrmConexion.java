    private void TxtIpKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_TxtIpKeyTyped
        char character = evt.getKeyChar();
        if (((character >= 'A') && (character <= 'Z')) || ((character >= 'a') && (character <= 'z'))) {
            evt.consume();
        }
    }//GEN-LAST:event_TxtIpKeyTyped


