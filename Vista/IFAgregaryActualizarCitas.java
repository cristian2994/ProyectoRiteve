    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (controladorCitas.comprobarPlaca(this)==false) {
            this.txtPlaca.setForeground(Color.green);
            this.controladorCitas.mostrarvehiculo(this);
            this.txtAntiguedad.setText(controladorCitas.antiguedad(this));
        }else{
            IFAgregarVehiculo vehi= new IFAgregarVehiculo();
            FrmMenuPrinc.escritorio.add(vehi);
            this.txtAntiguedad.setText(null);
             DefaultTableModel modelo= (DefaultTableModel) this.TablaVehiculo.getModel();
             modelo.setNumRows(0);
            vehi.show();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

